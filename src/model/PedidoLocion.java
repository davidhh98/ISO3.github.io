package model;

public class PedidoLocion {
	private int identificacion;
	private int locion_identificacion;
	private int cantidad;
	private int precio;
	public int getIdentificacion() {
		return identificacion;
	}
	public void setIdentificacion(int identificacion) {
		this.identificacion = identificacion;
	}
	public int getLocion_identificacion() {
		return locion_identificacion;
	}
	public void setLocion_identificacion(int locion_identificacion) {
		this.locion_identificacion = locion_identificacion;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public int getPrecio() {
		return precio;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	
	
}

