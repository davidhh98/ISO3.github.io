package model;

public class Tarjeta {
	private int identificacion;
	private int saldo;
	private int cliente_identificacion;
	public int getIdentificacion() {
		return identificacion;
	}
	public void setIdentificacion(int identificacion) {
		this.identificacion = identificacion;
	}
	public int getSaldo() {
		return saldo;
	}
	public void setSaldo(int saldo) {
		this.saldo = saldo;
	}
	public int getCliente_identificacion() {
		return cliente_identificacion;
	}
	public void setCliente_identificacion(int cliente_identificacion) {
		this.cliente_identificacion = cliente_identificacion;
	}
	
	
}
